    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        

    Connection conn = ConectarBD.conectar();
        PreparedStatement patm = null;
        ResultSet rs = null;
        
        Docente docente = new Docente();
        
        docente.setNome(cNome.getText());
        docente.setLogin(cLogin.getText());
        docente.setSenha(cSenha.getText());
        
      
   

        if ((cNome.getText().isEmpty()) || (cLogin.getText().isEmpty()))
                {
                    
            JOptionPane.showMessageDialog(null, "H√° campos vazios");
        } else {
                
                
            if (cSenha.getText().equals(cSenhaConf.getText())) {
               try{ 
                patm = (PreparedStatement) conn.prepareStatement(sql);
                patm.setString(1, cLogin.getText());
                rs = patm.executeQuery();  
                if (rs.next()) {
                    
                   JOptionPane.showMessageDialog(null, "Este Login j√° est√° sendo ultilizado", "Erro", JOptionPane.ERROR_MESSAGE);
                    cLogin.setText("");

                }else{    
                   
                DocenteDAO dao = new DocenteDAO();
                dao.inserirDocente(docente);
                JOptionPane.showMessageDialog(null, "Professor " + cNome.getText() + " inserido com sucesso! ");

                cNome.setText("");
                cLogin.setText("");
                cSenha.setText("");
                cSenhaConf.setText("");
                }
                
               }catch(SQLException e){
               
               
               e.printStackTrace();
            } finally {
                ConectarBD.desconectar(conn,patm ,rs );
    
            
            
            }
            } else {
                JOptionPane.showMessageDialog(null, "Os campos : Senha e Confirmar Senha  est√£o diferentes");

            }

        }

    
        



    }//GEN-LAST:event_jButton1ActionPerformed

